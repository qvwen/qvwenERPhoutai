package com.example.erp.liaoshuanchen.pojo;

public class Warehouse {
    private Integer warehouseId;

    private String warehouseName;

    private String warehouseAbbreviation;

    private String warehouseEnglish;

    private String warehouseContact;

    private String warehousePhone;

    private String warehouseAddress;

    private String warehouseRemark;

    public Integer getWarehouseId() {
        return warehouseId;
    }

    public void setWarehouseId(Integer warehouseId) {
        this.warehouseId = warehouseId;
    }

    public String getWarehouseName() {
        return warehouseName;
    }

    public void setWarehouseName(String warehouseName) {
        this.warehouseName = warehouseName;
    }

    public String getWarehouseAbbreviation() {
        return warehouseAbbreviation;
    }

    public void setWarehouseAbbreviation(String warehouseAbbreviation) {
        this.warehouseAbbreviation = warehouseAbbreviation;
    }

    public String getWarehouseEnglish() {
        return warehouseEnglish;
    }

    public void setWarehouseEnglish(String warehouseEnglish) {
        this.warehouseEnglish = warehouseEnglish;
    }

    public String getWarehouseContact() {
        return warehouseContact;
    }

    public void setWarehouseContact(String warehouseContact) {
        this.warehouseContact = warehouseContact;
    }

    public String getWarehousePhone() {
        return warehousePhone;
    }

    public void setWarehousePhone(String warehousePhone) {
        this.warehousePhone = warehousePhone;
    }

    public String getWarehouseAddress() {
        return warehouseAddress;
    }

    public void setWarehouseAddress(String warehouseAddress) {
        this.warehouseAddress = warehouseAddress;
    }

    public String getWarehouseRemark() {
        return warehouseRemark;
    }

    public void setWarehouseRemark(String warehouseRemark) {
        this.warehouseRemark = warehouseRemark;
    }
}
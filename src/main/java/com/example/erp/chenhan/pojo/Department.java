package com.example.erp.chenhan.pojo;

public class Department {
    private Integer departmentId;

    private String departmentName;

    private String departmentEnglish;

    private String departmentProgram;

    private String departmentCalendar;

    private String departmentRemarks;

    public Integer getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Integer departmentId) {
        this.departmentId = departmentId;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }

    public String getDepartmentEnglish() {
        return departmentEnglish;
    }

    public void setDepartmentEnglish(String departmentEnglish) {
        this.departmentEnglish = departmentEnglish;
    }

    public String getDepartmentProgram() {
        return departmentProgram;
    }

    public void setDepartmentProgram(String departmentProgram) {
        this.departmentProgram = departmentProgram;
    }

    public String getDepartmentCalendar() {
        return departmentCalendar;
    }

    public void setDepartmentCalendar(String departmentCalendar) {
        this.departmentCalendar = departmentCalendar;
    }

    public String getDepartmentRemarks() {
        return departmentRemarks;
    }

    public void setDepartmentRemarks(String departmentRemarks) {
        this.departmentRemarks = departmentRemarks;
    }
}